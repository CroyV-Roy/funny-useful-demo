var currDate = new Date();
var currMonth = currDate.getMonth();
var currYear = currDate.getFullYear();

generateCalendar(currYear, currMonth);

document.querySelector('#prev').onclick = () => {
    --currYear;
    generateCalendar(currYear, currMonth);
};

document.querySelector('#next').onclick = () => {
    ++currYear;
    generateCalendar(currYear, currMonth);
}


